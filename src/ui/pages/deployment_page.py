"""
Информационная страница о масштабируемости и вариантах развертывания.
"""
import streamlit as st

def render() -> None:
    st.title("🚀 Масштабируемость системы")
    st.markdown("""
    ## Архитектура развёртывания
    Система построена по модульному принципу с разделением на UI, бизнес-логику и модели.
    Благодаря использованию фабрик и единого интерфейса обработчиков, возможно лёгкое масштабирование:

    - **Добавление новых моделей**: достаточно зарегистрировать их в `model_registry.py`.
    - **Новые методы предобработки**: реализовать класс, наследующий `BasePreprocessingHandler`, и добавить в реестр фабрики.
    - **Расширение UI**: каждая страница независима, можно добавлять новые без изменения существующего кода.
    - **Развёртывание**: Streamlit-приложение может быть упаковано в Docker-контейнер и развёрнуто на облачных платформах (AWS, Azure, GCP) или на локальном сервере.

    ### Варианты развёртывания
    1. **Локальный запуск**: `streamlit run app.py`
    2. **Docker**: образ с Python 3.10 и зависимостями из `requirements.txt`
    3. **Streamlit Sharing / Community Cloud**: бесплатный хостинг Streamlit-приложений
    4. **Сервер с Nginx**: проксирование порта Streamlit с SSL

    ### Масштабирование вычислений
    Для больших датасетов и сложных моделей можно использовать:
    - параллельное обучение через `n_jobs` в sklearn;
    - распределённые вычисления с помощью Dask или Ray.

    Система готова к интеграции дополнительных библиотек и алгоритмов без переписывания интерфейса.
    """)