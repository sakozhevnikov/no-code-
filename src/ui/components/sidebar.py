"""Боковая навигация приложения."""
import streamlit as st

def render_sidebar() -> str:
    st.sidebar.title("🧭 Навигация")
    page = st.sidebar.radio(
        "Выберите раздел:",
        (
            "📂 Загрузка данных",
            "📊 Предварительный анализ",
            "🧹 Предобработка данных",
            "🤖 Машинное обучение",
            "🚀 Масштабируемость системы"
        )
    )
    return page